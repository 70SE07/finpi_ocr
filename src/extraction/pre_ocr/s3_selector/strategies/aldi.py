"""
Aldi Strategy - магазины Aldi.

Характеристики:
- Обычно более простая печать (может быть термобумага)
- Меньше требований к освещению
- Контраст может быть ниже
- Пороги немного ниже (более требовательно к обработке)
"""

from .base import AbstractStrategy


class AldiStrategy(AbstractStrategy):
    """
    Стратегия для магазинов Aldi.
    
    Aldi = более простая печать, может быть термобумага
    Пороги ниже = более агрессивная обработка
    """
    
    # Синие чернила: 20 (дефолт)
    BLUE_DOMINANCE_THRESHOLD = 20.0
    
    # Низкий контраст: 42 (выше дефолта 40 = менее терпимо к низкому контрасту)
    LOW_CONTRAST_THRESHOLD = 42.0
    
    # Контраст для CLAHE: 52 (выше дефолта 50)
    CLAHE_CONTRAST_THRESHOLD = 52.0
    
    # Шум для денойзирования: 1100 (выше дефолта 1000 = менее агрессивно удаляем шум)
    DENOISE_NOISE_THRESHOLD = 1100.0
    
    @property
    def name(self) -> str:
        return "Aldi"
