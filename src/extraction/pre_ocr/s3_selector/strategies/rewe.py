"""
Rewe Strategy - супермаркеты Rewe.

Характеристики:
- Обычно хорошее освещение в магазине
- Белая бумага высокого качества
- Контраст обычно хороший
- Поэтому пороги можно выше (менее требовательно к обработке)
"""

from .base import AbstractStrategy


class ReweStrategy(AbstractStrategy):
    """
    Стратегия для магазинов Rewe.
    
    Rewe = качественная печать, хорошее освещение
    Пороги выше = менее агрессивная обработка
    """
    
    # Синие чернила: 25 (немного выше дефолта 20)
    BLUE_DOMINANCE_THRESHOLD = 25.0
    
    # Низкий контраст: 35 (ниже дефолта 40 = более терпимо к низкому контрасту)
    LOW_CONTRAST_THRESHOLD = 35.0
    
    # Контраст для CLAHE: 45 (ниже дефолта 50)
    CLAHE_CONTRAST_THRESHOLD = 45.0
    
    # Шум для денойзирования: 900 (ниже дефолта 1000 = более агрессивно удаляем шум)
    DENOISE_NOISE_THRESHOLD = 900.0
    
    @property
    def name(self) -> str:
        return "Rewe"
