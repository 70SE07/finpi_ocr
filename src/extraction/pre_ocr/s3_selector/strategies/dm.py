"""
DM Strategy - аптеки DM.

Характеристики:
- Чеки часто с плохим контрастом
- Маленький шрифт
- Часто требуется агрессивная обработка
- Пороги значительно ниже дефолта (очень требовательно)
"""

from .base import AbstractStrategy


class DMStrategy(AbstractStrategy):
    """
    Стратегия для аптек DM.
    
    DM = часто плохой контраст, маленький шрифт
    Пороги значительно ниже = максимально агрессивная обработка
    """
    
    # Синие чернила: 18 (ниже дефолта 20)
    BLUE_DOMINANCE_THRESHOLD = 18.0
    
    # Низкий контраст: 48 (выше дефолта 40 = очень требовательно)
    LOW_CONTRAST_THRESHOLD = 48.0
    
    # Контраст для CLAHE: 55 (выше дефолта 50 = часто применяется)
    CLAHE_CONTRAST_THRESHOLD = 55.0
    
    # Шум для денойзирования: 800 (ниже дефолта 1000 = часто применяется)
    DENOISE_NOISE_THRESHOLD = 800.0
    
    @property
    def name(self) -> str:
        return "DM"
